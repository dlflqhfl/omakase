    private void D_change_emp_bt_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_D_change_emp_bt_saveActionPerformed
    	EmpVO evo = new EmpVO();
        
        String empno = D_change_emp_tf_empno.getText().trim();
        if(empno.length() == 0)
           empno = null;
        evo.setEmpno(Integer.parseInt(empno));
        
        String company = D_change_emp_tf_company.getText().trim();
        if(company.length() == 0)
           company = null;
        evo.setBelong(company);
        
        String ename = D_change_emp_tf_ename.getText().trim();
        if(ename.length() == 0)
           ename = null;
        evo.setEname(ename);
        
        String position = D_change_emp_cb_position.getSelectedItem().toString();
        if(position.length() == 0)
           position = null;
        evo.setPosition(position);
        
        
        String phone = D_change_emp_tf_phone.getText().trim();
        if(phone.length() == 0)
           phone = null;
        evo.setPhone(phone);
        
        String birth = D_change_emp_tf_birth.getText().trim();
        if(birth.length() == 0)
           birth= null;
        evo.setBirth(birth);
        
        String job = D_change_emp_tf_job.getText().trim();
        if(job.length() == 0)
           job = null;
        evo.setJob(job);
        
        String hiredate = D_change_emp_tf_hiredate.getText().trim();
        if(hiredate.length() == 0)
           hiredate = null;
        evo.setHiredate(hiredate);
        
        String hobby = D_change_emp_tf_hobby.getText().trim();
        if(hobby.length() == 0)
           hobby = null;
        evo.setHobby(hobby);
        
        String speciality = D_change_emp_tf_speciality.getText().trim();
        if(speciality.length() == 0)
           speciality = null;
        evo.setSpecialty(speciality);
        
        String add = D_change_emp_tf_add.getText().trim();
        if(add.length() == 0)
           add = null;
        evo.setAddress(add);
        
        String account = D_change_emp_tf_account.getText().trim();
        if(account.length() == 0)
           account = null;
        evo.setAccount_number(account);
        
        String venter = D_change_emp_cb_venter.getSelectedItem().toString();
        int v_value = venter.equals("Y")?1:0;
        evo.setVerterans(v_value);
        
        String obstacle = D_change_emp_cb_obstacle.getSelectedItem().toString();
        int o_value = obstacle.equals("Y")?1:0;
        evo.setObstacle(o_value);
        
        String bank = D_change_emp_cb_bank.getSelectedItem().toString();
        evo.setBank_info(bank);
        
        String employment = D_change_emp_cb_employment.getSelectedItem().toString();
        evo.setEmployment(employment);
        
        String gender = D_change_emp_cb_gender.getSelectedItem().toString();
        evo.setGender(gender);
        
        String dept = D_change_emp_cb_dept.getSelectedItem().toString(); //부서명
        
        SqlSession ss = Factory.getFactory().openSession();
        
        DeptVO dvo = ss.selectOne("dept.dept", dept);
        evo.setDvo(dvo);
        evo.setDeptno(dvo.getDeptno());
        
        Date currentDate = new Date();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        String currentDateString = formatter.format(currentDate);
        
        if (D_change_emp_cb_leave.isSelected()) {
            evo.setJoin_status("1");
            evo.setFire_date(currentDateString);
        } else {
            evo.setJoin_status("0");
            evo.setFire_date(null);
        }
        
        if(ss != null)
           ss.close();
        
        //public MemberVO(int empno, int a_cate, int deptno, String id, String pw)
        String id = D_change_emp_tf_id.getText().trim();
        if(id.length() == 0)
        id = null;

        String password = D_change_emp_tf_password.getText().trim();
        if(password.length() == 0)
        password = null;
         
        int auth = D_change_emp_cb_auth.getSelectedIndex();
        MemberVO mvo = new MemberVO(Integer.parseInt(empno), auth, dvo.getDeptno(), id, password);
        evo.setMvo(mvo);


 		
 		int cnt=this.updateEmp(evo);
 		if(cnt>0) {
 			JOptionPane.showMessageDialog(change_emp.this, "emp저장완료!");
 		}
 		
 		
 		int cnt2=this.updateMember(mvo);
 		if(cnt2>0) {
 			JOptionPane.showMessageDialog(change_emp.this, "member저장완료!");
 			}
    
 		
    }//GEN-LAST:event_D_change_emp_bt_saveActionPerformed

