    public int updateEmp(EmpVO evo) {
		SqlSession ss = Factory.getFactory().openSession();
		
		int cnt = ss.insert("emp.update", evo);
		
		if(cnt > 0)
			ss.commit();
		else
			ss.rollback();

		if(ss != null)
			ss.close();
		
		return cnt;
	}

    public int updateMember(MemberVO mvo) {
		SqlSession ss = Factory.getFactory().openSession();
		
		int cnt = ss.insert("member.update", mvo);
		
		if(cnt > 0)
			ss.commit();
		else
			ss.rollback();

		if(ss != null)
			ss.close();
		
		return cnt;
	}


